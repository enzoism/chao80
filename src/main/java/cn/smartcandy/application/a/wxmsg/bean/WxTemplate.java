public class WxTemplate {

	private String template_id;  //模板id
	private String touser;  //消息接收方
	private String url;  //模板消息详情链接	非必须
	private Map<String,TemplateData> data;
	
	private String branchSequence;  //机构/商户序号

	/**
	 * @return the template_id
	 */
	public String getTemplate_id() {
		return template_id;
	}

	/**
	 * @param template_id the template_id to set
	 */
	public void setTemplate_id(String template_id) {
		this.template_id = template_id;
	}

	/**
	 * @return the touser
	 */
	public String getTouser() {
		return touser;
	}

	/**
	 * @param touser the touser to set
	 */
	public void setTouser(String touser) {
		this.touser = touser;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the data
	 */
	public Map<String, TemplateData> getData() {
		return data;
	}

	/**
	 * @param data the data to set
	 */
	public void setData(Map<String, TemplateData> data) {
		this.data = data;
	}

	/**
	 * @return the branchSequence
	 */
	public String getBranchSequence() {
		return branchSequence;
	}

	/**
	 * @param branchSequence the branchSequence to set
	 */
	public void setBranchSequence(String branchSequence) {
		this.branchSequence = branchSequence;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "WxTemplate [template_id=" + template_id + ", touser=" + touser + ", url=" + url + ", data=" + data
				+ ", branchSequence=" + branchSequence + "]";
	}
}
