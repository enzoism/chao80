public class BusinessSort implements Serializable {

	private static final long serialVersionUID = -5333232088244774965L;

	/**业务类*/
	public static final String BS_BIZTYPE_BIZ="B";
	/**公共业务类*/
	public static final String BS_BIZTYPE_COMMON="C";
	/**系统类*/
	public static final String BS_BIZTYPE_SYS="S";

	private int cbs_sequence; // 业务序号
	private String cbs_code; // 业务类型代码
	private String cbs_name; // 业务类型名称
	private String cbs_bizType; // 业务类别 "B-业务类；C-公共业务类；S-公共类；"
	private boolean cbs_auditType; // 是否需要多级审核 "0-否；1-是；"
	private int cbs_auditGrade; // 业务需审核级数"0-无；1-一级审批；2-二级审批；3-三级审批；4-四级审批；"
	private String cbs_property;// 业务属性
	private String cbs_finalStatus;// 业务最终状态

	public int getCbs_sequence() {
		return cbs_sequence;
	}

	public void setCbs_sequence(int cbs_sequence) {
		this.cbs_sequence = cbs_sequence;
	}

	public String getCbs_code() {
		return cbs_code;
	}

	public void setCbs_code(String cbs_code) {
		this.cbs_code = cbs_code;
	}

	public String getCbs_name() {
		return cbs_name;
	}

	public void setCbs_name(String cbs_name) {
		this.cbs_name = cbs_name;
	}

	public boolean isCbs_auditType() {
		return cbs_auditType;
	}

	public void setCbs_auditType(boolean cbs_auditType) {
		this.cbs_auditType = cbs_auditType;
	}

	public int getCbs_auditGrade() {
		return cbs_auditGrade;
	}

	public void setCbs_auditGrade(int cbs_auditGrade) {
		this.cbs_auditGrade = cbs_auditGrade;
	}

	public String getCbs_bizType() {
		return cbs_bizType;
	}

	public void setCbs_bizType(String cbs_bizType) {
		this.cbs_bizType = cbs_bizType;
	}

	public String getCbs_property() {
		return cbs_property;
	}

	public void setCbs_property(String cbs_property) {
		this.cbs_property = cbs_property;
	}

	public String getCbs_finalStatus() {
		return cbs_finalStatus;
	}

	public void setCbs_finalStatus(String cbs_finalStatus) {
		this.cbs_finalStatus = cbs_finalStatus;
	}
}
